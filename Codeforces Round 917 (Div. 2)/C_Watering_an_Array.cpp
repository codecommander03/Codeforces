void solve(){
    int n, k, d, res = 0;
    std::cin>>n>>k>>d;
    std::vector<int> a(n), b(k);
    for(int i=0; i<n; i++) std::cin>>a[i];
    for(int i=0; i<k; i++) std::cin>>b[i];

    for (int i = 0; i < d;i++){
        if(n+(d-i-1)/2 <= res) break; // if the maximum possible value is less than the current value of res, then break the loop
        int curr = 0;
        for (int j = 0; j < n;j++){
            curr += (a[j] == j + 1);
        }
        res = std::max(res, curr + (d-i-1)/2);
        for (int j = 0; j < b[i%k];j++) {
            a[j]++;
        }
    }

    std::cout << res << '\n';
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
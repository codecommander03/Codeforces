void solve(){
    int n, m;
    std::cin >> n >> m;
    auto query = [&](int x, int y)
    {
        if (x < 1 || x > n || y < 1 || y > m)
            return -1;
        std::cout << "? " << x << ' ' << y << "\n";
        std::cout.flush();
        int z;
        std::cin >> z;
        return z;
    };
    int d = query(1, 1);
    std::cout.flush();
    int lx = std::max(1, 2 + d - m);
    int p = query(lx, 2 + d - lx);
    std::cout.flush();
    int rx = std::max(1, 2 + d - n);
    int q = query(2 + d - rx, rx);
    if (query(lx + p / 2, 2 + d - lx - p / 2) == 0)
    {
        std::cout << "! " << lx + p / 2 << " " << 2 + d - lx - p / 2 << "\n";
    }
    else
    {
        std::cout << "! " << 2 + d - rx - q / 2 << " " << rx + q / 2 << "\n";
    }
    std::cout.flush();
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
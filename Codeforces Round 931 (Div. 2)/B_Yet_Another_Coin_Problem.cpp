int help(int n){
    int res = 0;
    res += n / 15;
    n %= 15;
    res += n / 6;
    n %= 6;
    res += n / 3;
    n %= 3;
    res += n;
    return res;
}

void solve(){
    int n;
    std::cin>>n;
    int res = help(n);
    if(n>=10) res = std::min(res,help(n-10)+1);
    if(n>=20) res = std::min(res,help(n-20)+2);
    std::cout<<res<<'\n';
}
 

int f(int goal, int a, int b)
{
    if (a == b)
        return 1;
    if (a % 2 != b % 2)
        return 0;
    a -= b;
    goal += b;
    b = 0;
    if(goal)
        return 1;
    return 0;
}

void solve()
{
    int a, b, c;
    std::cin >> a >> b >> c;
    std::cout << f(a, std::max(b, c), std::min(b, c)) << " ";
    std::cout << f(b, std::max(a, c), std::min(a, c)) << " ";
    std::cout << f(c, std::max(a, b), std::min(a, b)) << "\n";
}

int32_t main()
{
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t = 1;
    std::cin >> t;
    while (t--)
        solve();
}
void solve()
{
    int n;
    std::string s;
    std::cin >> n >> s;
    std::vector<int> l(n), r(n);
    for (int i = 0; i < n; i++)
    {
        std::cin >> l[i] >> r[i];
        l[i]--;
        r[i]--;
    }
    auto dfs = [&](auto self, int x) -> int
    {
        if (l[x] == -1 && r[x] == -1)
            return 0;
        int ans = n;
        if (l[x] != -1)
        {
            ans = self(self, l[x]) + (s[x] != 'L');
        }
        if (r[x] != -1)
        {
            ans = std::min(ans, self(self, r[x]) + (s[x] != 'R'));
        }
        return ans;
    };
    int res = dfs(dfs, 0);
    std::cout << res << '\n';
}

int32_t main()
{
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t = 1;
    std::cin >> t;
    while (t--)
        solve();
}
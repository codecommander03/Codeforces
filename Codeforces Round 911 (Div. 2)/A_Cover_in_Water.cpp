void solve(){
    int n,res=0;
    std::string s;
    std::cin >> n >> s;

    for (int i = 0; i < n;i++){
        if(s[i] == '#')
            continue;
        int j = i + 1;
        while(j < n && s[j] == '.')
            j++;
        if(j-i>=3){
            std::cout << "2\n";
            return;
        }
        res += j - i;
        i = j;
    }
    std::cout << res << '\n';
}
 

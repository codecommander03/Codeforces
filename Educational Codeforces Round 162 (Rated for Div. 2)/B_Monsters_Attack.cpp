void solve(){
    int n, k;
    std::cin>>n>>k;
    std::vector<int> a(n), x(n);
    for(int i=0;i<n;i++) std::cin>>a[i];
    for(int i=0;i<n;i++) std::cin>>x[i];

    std::map<int, int> m;
    for(int i=0;i<n;i++){
        m[std::abs(x[i])] += a[i];
    }

    int prev = 0;
    int power = 0;
    for(auto i=m.begin();i!=m.end();i++){
        int curr = i->first, hp = i->second;
        power += (curr - prev) * k;
        if(power < hp){
            std::cout<<"NO\n";
            return;
        }
        power -= hp;
        prev = curr;
    }
    std::cout<<"YES\n";
}
 

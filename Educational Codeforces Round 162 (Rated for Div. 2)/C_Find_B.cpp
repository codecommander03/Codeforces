void solve(){
    int n, q,x;
    std::cin>>n>>q;
    std::vector<int> sum(n+1),one(n+1);
    for(int i=1;i<=n;i++){
        std::cin>>x;
        sum[i] = sum[i-1] + x;
        one[i] = one[i-1] + (x==1);
    }
    
    while(q--){
        int l, r;
        std::cin>>l>>r;
        int len = r-l+1, curr_sum = sum[r] - sum[l-1], ones = one[r] - one[l-1];
        if(len+ones<=curr_sum && l!=r)
            std::cout << "YES\n";
        else std::cout << "NO\n";
    }
}
 

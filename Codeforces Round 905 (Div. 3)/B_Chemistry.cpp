void solve(){
    int n, k;
    std::string s;
    std::cin>>n>>k>>s;

    std::unordered_map<char, int> m;
    for(int i=0; i<n; i++){
        m[s[i]]++;
    }
    int ones = 0, pairs = 0;
    for(auto i: m){
        pairs += i.second/2;
        ones += i.second%2;
    }
    if(k==0){
        if(ones<=1)
            std::cout << "YES\n";
        else 
            std::cout << "NO\n";
        return;
    }
    if(ones<=k){
        // if(2*pairs<k-ones){
        //     std::cout << "NO\n";
        // }
        // else 
        std::cout << "YES\n";
        return;
    }
    if(ones<=k+1){
        std::cout << "YES\n";
        return;
    }
    std::cout << "NO\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
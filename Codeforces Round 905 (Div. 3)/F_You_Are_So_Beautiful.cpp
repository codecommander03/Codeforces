void solve(){
    int n;
    std::cin>>n;
    std::vector<int> v(n), a(n);
    std::map<int, int> m;

    int res = 0, cnt = 0;
    for (int i = 0; i < n;i++) {
        std::cin >> v[i];
        int x = v[i];
        v[i] = m.find(x) == m.end();
        m[x] = i;
    }
    for(auto i: m)
        a[i.second] = 1;
    for (int i = n - 1; i >= 0;i--){
        cnt += a[i];
        if(v[i]) res += cnt;
    }
    std::cout << res << '\n';
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
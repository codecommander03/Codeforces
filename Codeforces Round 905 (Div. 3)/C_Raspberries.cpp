void solve(){
    int n, k;
    std::cin>>n>>k;
    std::vector<int> v(n);
    for(int i=0; i<n; i++) std::cin>>v[i];

    int res = k, cnt = 0;
    for(int i=0; i<n; i++){
        if(v[i]%k==0){
            std::cout<<0<<'\n';
            return;
        }
        if(v[i]%2==0)
            cnt++;
        res = std::min(res, k-v[i]%k);
    }
    if(k==4){
        if(cnt>=2) res = 0;
        else if(cnt == 1)
            res = 1;
        else
            res = std::min(res, 2LL);
    }
    std::cout<<res<<'\n';
}
 

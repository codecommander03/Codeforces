void solve()
{
    long long n, x, y;
    std::cin >> n >> x >> y;
    //int add = n / x, sub = n / y, k = x * y / std::__gcd(x, y);
    // long long int ans = sum[n] - sum[n - add] - sum[sub];
    // add -= n / k;
    // sub -= n / k;
    long long add = n / x - n / (x * y / std::__gcd(x, y));
    long long sub = n / y - n / (x * y / std::__gcd(x, y));

    long long ans = 0;
    ans += add * (n + n - add + 1) / 2;
    ans -= sub * (sub + 1) / 2;

    std::cout << ans << '\n';
    //std::cout << add << " " << sub << " " << k << '\n';
}


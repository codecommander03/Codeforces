void solve(){
    int n;
    std::cin>>n;
    std::vector<int> v(n);
    for(int i=0;i<n;i++) std::cin>>v[i];

    for (int k = 2; k <= 1e18;k*=2) {
        std::set<int> s;
        for(int i: v)
            s.insert(i % k);
        if(s.size() == 2){
            std::cout << k << "\n";
            return;
        }
        s.clear();
    }
}
 

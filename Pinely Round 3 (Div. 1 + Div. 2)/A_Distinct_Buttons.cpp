void solve()
{
    int n, x, y;
    std::cin >> n;
    bool xn = false, xp = false, yn = false, yp = false;
    while (n--)
    {
        std::cin >> x >> y;
        if (x < 0)
            xn = true;
        if (x > 0)
            xp = true;
        if (y < 0)
            yn = true;
        if (y > 0)
            yp = true;
    }
    std::cout << ((xn && xp && yn && yp) ? "NO" : "YES") << '\n';
}

int32_t main()
{
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t = 1;
    std::cin >> t;
    while (t--)
        solve();
}
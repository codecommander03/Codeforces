void solve(){
    int n;
    std::cin>>n;
    std::vector<int> v(n);
    for(int i=0;i<n;i++){
        std::cin>>v[i];
    }
    std::sort(begin(v), end(v));
    v.resize(std::unique(begin(v), end(v))-begin(v));

    int res = 0, prev = 0;
    for (int i = 0; i < v.size();i++){
        while(v[i] - v[prev] >= n)
            prev++;
        res = std::max(res, i - prev + 1);
    }
    std::cout<<res<<"\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
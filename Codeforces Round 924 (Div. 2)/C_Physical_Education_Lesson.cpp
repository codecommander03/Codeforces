void solve(){
    int n, x;
    std::cin>>n>>x;
    n--, x--;
    int f1 = n - x, f2 = n + x;
    std::vector<int> v;
    for (int i = 1; i * i <= f1;i++){
        if(f1 % i==0){
            v.push_back(i);
            if(i * i != f1) v.push_back(f1 / i);
        }
    }
    for (int i = 1; i * i <= f2;i++){
        if(f2 % i==0){
            v.push_back(i);
            if(i * i != f2) v.push_back(f2 / i);
        }
    }
    std::set<int> s;
    for(int i: v){
        if(i%2)
            continue;
        if(i/2 + 1 > x)
            s.insert(1 + i / 2);
    }
    std::cout<<s.size()<<"\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
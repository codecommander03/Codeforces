int help(int i,int x){
    return (x - (i % x)) % x;
}
void solve()
{
    int n, x, y, temp,res = 0;
    std::cin >> n >> x >> y;
    std::vector<int> v(n);
    std::map<int,std::map<int,int>> m;
    for (int i = 0; i < n; i++)
    {
        std::cin >> v[i];
        m[v[i]%x][v[i]%y]++;
    }
    for(int &i: v){
        res += m[help(i,x)][i%y] - (i % x == help(i,x));
    }
    std::cout << res/2 << '\n';
}


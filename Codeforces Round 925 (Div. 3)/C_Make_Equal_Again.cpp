void solve(){
    int n;
    std::cin >> n;
    std::vector<int> v(n);
    for(int i = 0; i < n; i++){
        std::cin >> v[i];
    }
    if (n == 1){
        std::cout << 0 << '\n';
        return;
    }

    int l = 0, r = n - 1;
    while(v[l] == v[0])
        ++l;
    while(v[r] == v[n-1])
        --r;
    if(v[0] != v[n-1]){
        std::cout << std::min(n-l,r+1) << '\n';
        return;
    }
    std::cout<< std::max(0LL, r-l+1) << '\n';
}
 

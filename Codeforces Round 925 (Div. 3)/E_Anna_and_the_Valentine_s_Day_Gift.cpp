auto l(int x) {
    return int(log10(x) + 1);
};

auto help(int x) {
    int z = 1, r = 0;
    while (x % (z *= 10) == 0)
        r++;
    return r;
};

void solve(){
    int n, m, res = 0;
    std::cin>>n>>m;
    std::vector<int> v(n);
    for(int i=0; i<n; i++){
        std::cin>>v[i];
    }
    std::sort(begin(v), end(v), [&](const int &a, const int &b)
              { return help(a) < help(b); });

    for (int i = n - 1; i >= 0; i--){
        res += l(v[i]);
        if(i % 2 == (n-1)%2) res -= help(v[i]);
    }
    std::cout << (res <= m ? "Anna" : "Sasha") << '\n';
}
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
void solve(){
    int sum;
    int a = 0, b = 0, c = 0;
    std::cin >> sum;
    if(sum > 28){
        sum -= 26;
        c = 26;
        if(sum>27){
            sum -= 26;
            b = 26;
            a = sum;
        }
        else{
            b = sum-1;
            a = 1;
        }
    }
    else{
        c = sum-2;
        sum = 0;
        a = 1;
        b = 1;
    }
    std::cout << char( 'a'+a - 1) << char('a' + b - 1) << char('a' + c - 1) <<"\n";
}
 

std::vector<int> sum(2e5 + 1, 0);
int help(int n)
{
    int sum = 0;
    while (n > 0)
    {
        sum += n % 10;
        n /= 10;
    }
    return sum;
}

void solve()
{
    int n;
    std::cin >> n;
    std::cout << sum[n] << std::endl;
}

int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);
    for (int i = 1; i <= 2e5;i++)
        sum[i] = sum[i-1]+help(i);

    int t = 1;
    std::cin>>t;
    while(t--) solve();
}
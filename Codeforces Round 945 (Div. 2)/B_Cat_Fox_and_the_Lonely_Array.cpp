void solve(){
    int n;
    std::cin>>n;
    std::vector<std::vector<int>> v(n,std::vector<int>(32,0));
    for(int i=0; i<n; i++) {
        int x, j=0;
        std::cin>>x;
        while(x){
            if(x%2) v[i][j]++;
            x/=2;
            j++;
        }
    }

    int l = 1, r = n;
    while(l<=r){
        int mid = l + (r-l)/2;
        bool flag = true;

        std::vector<int> a(32,0);
        int num = 0;

        for (int i = 0; i < mid;i++){
            for (int j = 0; j < 32;j++){
                if(v[i][j]) a[j]++;
            }
        }
        //s.insert(a);
        for (int i = mid; i < n;i++){
            for (int j = 0; j < 32;j++){
                int prev = a[j];
                if(v[i-mid][j]) {
                    a[j]--;
                }
                if (v[i][j]){
                    a[j]++;
                }
                if(prev==0 && a[j] || prev && a[j]==0){
                    flag = false;
                    break;
                }
            }
        }
        if(flag) r = mid - 1;
        else l = mid + 1;
    }
    std::cout<<l<<'\n';
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
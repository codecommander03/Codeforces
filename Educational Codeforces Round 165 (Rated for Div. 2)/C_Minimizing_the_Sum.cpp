void solve(){
    int n, k;
    std::cin>>n>>k;
    std::vector<int> v(n);
    for(int i=0; i<n; i++) std::cin>>v[i];

    std::vector<std::vector<int>> dp(n+1, std::vector<int>(k+1, 1e18));
    dp[0][0] = 0;

    for (int i = 0; i < n;i++){
        for (int h = 0; h <= k;h++){
            int mn = v[i];
            for (int j = i;j<n && h+j-i<=k;j++){ // i to j min. element
                mn = std::min(mn, v[j]);
                dp[j + 1][h + j - i] = std::min(dp[j + 1][h + j - i], dp[i][h] + mn * (j - i + 1));
            }
        }
    }
    std::cout<<*min_element(begin(dp[n]),end(dp[n]))<<"\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
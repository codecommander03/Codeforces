void solve(){
    int n,x;
    std::cin >> n;
    int sum_a = 0, sum_b = 0;
    int min_a = 1e9, min_b = 1e9;
    for (int i = 0; i < n;i++){
        std::cin >> x;
        sum_a += x;
        if(x<min_a)
            min_a = x;
    }
    for (int i = 0; i < n; i++)
    {
        std::cin >> x;
        sum_b += x;
        if (x < min_b)
            min_b = x;
    }
    std::cout << std::min(sum_a + n*min_b, sum_b + n*min_a) << "\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
void solve(){
    int n, m, res = 1e9, curr=0;
    std::cin>>n>>m;
    std::vector<int> a(n), b(n);
    for(int i=0; i<n; i++) std::cin>>a[i];
    for(int i=0; i<n; i++) std::cin>>b[i];
    m--;
    for (int i = n - 1; i > m;i--){
        curr += std::min(a[i], b[i]);
    }
    int mid = 0, top = 1e9;
    for (int i = m; i >= 0;i--){
        top = std::min(mid+a[i],top);
        mid += b[i];
    }
    std::cout << curr + top << '\n';
}

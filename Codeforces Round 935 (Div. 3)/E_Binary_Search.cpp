void solve(){
    int n, x;
    std::cin>>n>>x;
    std::vector<int> p(n+1);
    for(int i=1; i<=n; i++) std::cin>>p[i];
    int pos = -1;
    for (int i = 0; i <= n;i++){
        if(p[i]==x){
            pos = i;
        }
    
    }

    std::vector<bool> vis(n + 1, false);
    int l = 1, r = n+1;
    while(r > l+1){
        int m = (l + r) / 2;
        vis[m] = true;
        if(p[m] <= x)
            l = m;
        else
            r = m;
    }
    if(pos == l){
        std::cout << "0\n";
        return;
    }

    std::vector<std::pair<int, int>> res;
    res.push_back({pos, l});
    if(vis[pos]){
        for (int i = 1; i <= n;i++){
            if(p[i]<=x && vis[i]==false){
                res.push_back({i, pos});
                break;
            }
        }
    }
    std::cout<< res.size() << "\n";
    for(auto i: res){
        std::cout << i.first << " " << i.second << "\n";
    }
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
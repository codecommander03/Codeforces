void solve(){
    int n, res = -1;
    std::string s;
    std::cin>>n>>s;
    int l=0, r = 0;
    for(int i=0; i<n; i++){
        r += (s[i] == '1');
    }
    if(r >= (n+1)/2)
        res = 0;
    double centr = n / 2.0;
    for(int i=1; i<=n; i++){
        if(s[i-1] == '0'){
            l++;
        } else {
            r--;
        }
        if(l >= (i+1)/2 && r >= (n-i+1)/2){
            if(abs(centr - i) < abs(centr - res)){
                res = i;
            }
        }
    }
    std::cout<<res<<"\n";
}
 

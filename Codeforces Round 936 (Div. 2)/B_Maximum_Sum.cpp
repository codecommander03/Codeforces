int maxs(std::vector<int> &v){
    int curr = 0, res = 0;
    for(int i: v) {
        curr += i;
        res = std::max(res, curr);
        if(curr < 0) curr = 0;
    }
    return res;
}
void solve(){
    int n, k,sum = 0,mod=1e9+7;
    std::cin>>n>>k;
    std::vector<int> v(n);
    for(int i=0; i<n; i++) {
        std::cin>>v[i];
        sum  = (sum+v[i]);
    }
    int sub = maxs(v);
    int rem = sum - sub;
    while(k--){
        sub = (sub * 2) % mod;
    }
    std::cout<<((sub+rem)%mod+mod)%mod<<"\n";
}
 

void solve(){
    int n, k, m;
    std::string s;
    std::cin>>n>>k>>m>>s;

    std::set<char> vis;
    std::string res;

    for(char c: s){
        if(vis.find(c) == vis.end()) vis.insert(c);
        if(vis.size() == k){
            res += c;
            vis.clear();
        }
        if(res.size() == n){
            std::cout << "YES\n";
            return;
        }
    }
    std::cout << "NO\n";
    for (char c = 'a'; c < char(k+'a');c++){
        if(vis.find(c) == vis.end()){
            while(res.size() < n) res += c;
        }
    }
    std::cout << res << "\n";
}
 

void solve(){
    int n;
    std::cin >> n;

    char tr;
    std::cin >> tr;

    std::vector<std::string> cards(2 * n);
    std::map<char, std::vector<std::string>> track;
    for (int i = 0; i < 2 * n; i++)
    {
        std::cin >> cards[i];
        track[cards[i][1]].push_back(cards[i]);
    }
    for (auto &i : track)
        sort(i.second.begin(), i.second.end());

    int odds = 0;
    for (auto &i : track)
        if (i.first != tr)
            odds += i.second.size() % 2;

    if (track[tr].size() < odds || (track[tr].size() - odds) % 2 > 0)
    {
        std::cout << "IMPOSSIBLE\n";
        return;
    }
    std::vector<std::string> oddeds;
    for (auto &i : track)
        if (i.first != tr)
        {
            for (int j = 0; j + 1 < i.second.size(); j += 2)
                std::cout << i.second[j] << " " << i.second[j + 1] << "\n";
            if (i.second.size() % 2 > 0)
                oddeds.push_back(i.second.back());
        }

    int i = 0, j = 0;
    while (i < oddeds.size())
        std::cout << oddeds[i++] << " " << track[tr][j++] << "\n";

    for (; j + 1 < track[tr].size(); j += 2)
        std::cout<<track[tr][j]<<" "<<track[tr][j+1]<<"\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
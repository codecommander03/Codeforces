void solve(){
    int n, m, p = 1;
    std::string s;
    std::cin>>n>>m;
    
    std::vector<int> v(n);
    for(int i=0; i<n; i++){
        std::cin>>v[i];
    }
    std::cin>>s;
    int l = 0, r = n - 1;
    std::vector<int> a;
    for(char c: s){
        a.push_back(c == 'L' ? l++ : r--);
    }
    reverse(a.begin(), a.end());
    int res = 1;
    std::vector<int> b;
    for(int i: a){
        res *= v[i];
        res %= m;
        b.push_back(res);
    }
    reverse(b.begin(), b.end());
    for(int i: b){
        std::cout<<i<<" ";
    }
    std::cout << '\n';
}

int N = 4e5 + 1;
int pre[400005], v[400005];

void solve()
{
    int n;
    std::string s;
    std::cin >> n >> s;
    s = " " + s;

    pre[0] = v[0] = 0;
    for (int i = 1; i <= n; i++)
        pre[i] = pre[i - 1] + (s[i] - '0');
    for (int i = n; i > 0; i--)
    {
        v[i] = pre[i] % 10;
        pre[i - 1] += pre[i] / 10;
    }
    v[0] = pre[0];
    for (int i = 0, d = 0; i <= n; i++)
    {
        if (d || v[i])
        {
            std::cout << v[i];
            d = 1;
        }
    }
    std::cout << "\n";
}

int32_t main()
{
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t = 1;
    std::cin >> t;
    while (t--)
        solve();
}
void solve(){
    int n;
    std::cin>>n;
    std::vector<int> v(n);
    for(int &i: v) std::cin>>i;

    int ans=-1;
    for (int k = 1; k <= n;k++){
        if (n % k) continue;
        int mi = 1e18, ma = -1e18;
        for (int i = 0; i < n;i+=k){
            int sum = 0;
            for (int j = i; j < i + k;j++){
                sum += v[j];
            }
            mi = std::min(mi, sum);
            ma = std::max(ma, sum);
        }
        ans = std::max(ans, ma - mi);
    }
    std::cout<<ans<<"\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
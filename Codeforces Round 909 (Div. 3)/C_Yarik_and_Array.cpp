void solve(){
    int n;
    std::cin>>n;
    std::vector<int> v(n);
    for(auto &x:v) std::cin>>x;
    int ans=v[0];
    std::vector<int> dp(n,-1e9);
    dp[0]=v[0];
    for(int i=1;i<n;i++){
        if((v[i-1]%2+2)%2 != (v[i]%2+2)%2){
            dp[i] = dp[i - 1] + v[i];
        }
        dp[i] = std::max(v[i], dp[i]);
        ans = std::max(ans, dp[i]);
    }
    std::cout<<ans<<"\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}


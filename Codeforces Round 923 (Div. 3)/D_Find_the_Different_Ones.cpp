void solve(){
    int n,q,l,r;
    std::cin >> n;
    std::vector<int> v(n);
    for (int i = 0; i < n;i++){
        std::cin >> v[i];
    }
    std::vector<int> arr1(n, -1),arr2(n,-1);
    int i = 0;
    while(i<n){
        int j = i + 1;
        while(j<n && v[j]==v[i]){
            j++;
        }
        if(j<n){
            while(i<n &&i<j){
                arr1[i] = j;
                i++;
            }
        }
        else
            break;
    }
    i = n - 1;
    while(i>=0){
        int j = i - 1;
        while(j>=0 && v[j]==v[i]){
            j--;
        }
        if(j>=0){
            while(i>=0 && i>j){
                arr2[i] = j;
                i--;
            }
        }
        else
            break;
    }
    std::cin >> q;
    while(q--){
        std::cin >> l >> r;
        l--;
        r--;
        if(arr1[l] != -1 && arr1[l]<=r){
            std::cout << l + 1 << " " << arr1[l] + 1 << "\n";
        }
        else if(arr2[r] != -1 && arr2[r]>=l){
            std::cout << r + 1 << " " << arr2[r] + 1 << "\n";
        }
        else{
            std::cout << "-1 -1\n";
        }
    }
    std::cout << "\n";
}
 

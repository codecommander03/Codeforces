void solve(){
    int n, m, k, x;
    std::cin >> n >> m >> k;
    std::vector<int> v(k + 1, 0);
    for (int i = 0; i < n;i++){
        std::cin >> x;
        if(x<=k){
            v[x] = 1;
        }
    }

    for (int i = 0; i < m;i++){
        std::cin >> x;
        if(x<=k){
            if(v[x]==0){
                v[x] = 2;
            }
            else if(v[x]==1){
                v[x] = 3;
            }
        }
    }

    int a = 0, b = 0, c = 0;
    for (int i = 1; i <= k;i++){
        if(v[i]==0){
            std::cout << "NO\n";
            return;
        }
        if(v[i]==1) a++;
        else if(v[i]==2) b++;
        else if(v[i]==3) c++;
    }
    if(a<=k/2 && b<=k/2 && k-a-b<=c) std::cout << "YES\n";
    else if(a<=k/2 && b>=k/2 && c>=k/2-a) std::cout << "YES\n";
    else if(a>=k/2 && b<=k/2 && c>=k/2-b) std::cout << "YES\n";
    else std::cout << "NO\n";
    //std::cout << a << " " << b << " " << c << "\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
void solve(){
    char c = 'a';
    std::unordered_map<char, int> m;
    int n,x;
    std::cin>>n;
    std::string s;
    while(n--){
        std::cin >> x;
        if(x==0){
            s += c;
            //std::cout << n << " " << c << "\n";
            m[c] = 1;
            c++;
            
        }
        else{
            for(auto &i: m){
                if(i.second == x){
                    s += i.first;
                    i.second++;
                    break;
                }
            }
        }
    }
    std::cout << s << "\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
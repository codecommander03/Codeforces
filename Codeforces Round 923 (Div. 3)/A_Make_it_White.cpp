void solve(){
    int n;
    std::string s;
    std::cin >> n >> s;
    int l = -1, r = -1;
    for (int i = 0; i < n;i++){
        if(s[i]=='B'){
            l = i;
            break;
        }
    }
    for (int i = n-1; i >=0; i--)
    {
        if (s[i] == 'B')
        {
            r = i;
            break;
        }
    }
    if(l==-1){
        std::cout << 0 << '\n';
    }
    else {
        std::cout << r - l + 1 << '\n';
    }
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
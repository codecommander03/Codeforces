void solve(){
    int n;
    std::cin>>n;
    std::vector<int> v(n);
    for(int i=0; i<n; i++) std::cin>>v[i];
    std::sort(v.begin(), v.end());
    if(n==1){
        std::cout<<"1\n";
        return;
    }
    int f = v[1] - v[0];
    for(int i=2; i<n; i++){
        f = std::__gcd(f, v[i] - v[i-1]);
    }
    int res = 0;
    std::vector<int> a(n + 1);
    for (int i = 0; i < n;i++){
        int x = (v[n - 1] - v[i]) / f;
        if(x<=n){
            a[x] = 1;
        }
        res += x;
    }
    int i = 0;
    while(a[i]){
        i++;
    }
    res += i;
    std::cout<<res<<'\n';
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
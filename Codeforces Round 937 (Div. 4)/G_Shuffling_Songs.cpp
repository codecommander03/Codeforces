void solve(){
    int n;
    std::cin>>n;
    std::unordered_map<string, int> gc, wc;
    std::vector<std::pair<std::string, std::string>> songs(n);

    for (int i = 0; i < n;i++){
        std::string gen, wr;
        std::cin>>gen>>wr;
        songs[i] = {gen, wr};
        gc[gen]++;
        wc[wr]++;
    }
}
 

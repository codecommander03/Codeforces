void solve(){
    std::string s;
    std::cin>>s;
    int h = stoi(s.substr(0, 2));
    if(h==0){
        std::cout<<"12:"<<s.substr(3,2)<<" AM\n";
    }
    else if(h>12){
        int extra = h - 12;
        if(extra>=10){
            std::cout<<extra;
        }
        else{
            std::cout<<"0"<<extra;
        }
        std::cout << ":" << s.substr(3, 2) << " PM\n";
    }
    else if(h==12){
        std::cout<<"12:"<<s.substr(3,2)<<" PM\n";
    }
    else{
        int extra = h;
        if(extra>=10) std::cout<<extra;
        else std::cout<<"0"<<extra;
        std::cout<<":"<<s.substr(3,2)<<" AM\n";
    }
}
 

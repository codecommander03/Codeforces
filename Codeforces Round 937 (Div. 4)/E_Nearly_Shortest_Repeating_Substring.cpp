void solve(){
    int n;
    std::string s;
    std::cin>>n>>s;
    int res = n;
    for (int i = 1; i <= n/2;i++){
        if(n%i) continue;
        int x = 0;
        for (int j = i; j < n;j++){
            if(s[j] != s[j%i])
                x++;
            if(x>1)
                break;
        }
        if(x<=1){
            res = std::min(res, i);
            break;
        }
    }
    std::reverse(begin(s), end(s));
    for (int i = 1; i <= n / 2; i++)
    {
        if (n % i)
            continue;
        int x = 0;
        for (int j = i; j < n; j++)
        {
            if (s[j] != s[j % i])
                x++;
            if (x > 1)
                break;
        }
        if (x <= 1)
        {
            res = std::min(res, i);
            break;
        }
    }
    std::cout << res << "\n";
}
 

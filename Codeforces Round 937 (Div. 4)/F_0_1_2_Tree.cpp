void solve(){
    int a, b, c;
    std::cin>>a>>b>>c;
    if(c!=a+1){
        std::cout<<"-1\n";
        return;
    }
    if(a+b+c==1){
        std::cout<<"0\n";
        return;
    }
    int curr = 1,res=1,next=0;
    for (int i = 0; i < a + b;i++){
        if(curr == 0){
            std::swap(curr, next);
            res++;
        }
        curr--;
        next++;
        if(i<a)
            next++;
    }
    std::cout<<res<<"\n";
}
 

int N = 1e5;
int tob(int n){
    std::string s;
    while(n){
        s = (n % 2 ? "1" : "0") + s;
        n /= 2;
    }
    int x = stol(s);
    return x;
}
std::unordered_map<int, bool> m;
void solve(){
    int n;
    std::cin>>n;
    if(m.find(n) != m.end())
        std::cout<<"YES\n";
    else
        std::cout<<"NO\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);
    for (int a = 1; a <= 31;a++){
        int a2 = tob(a);
        if(a2 > N)
            break;
        for (int b = 1; b <= 31; b++){
            int b2 = tob(b);
            if(a2*b2 > N)
                break;
            for (int c = 1;c<=31;c++){
                int c2 = tob(c);
                if(a2 *b2*c2 >N)
                    break;
                for(int d = 1; d <= 31; d++){
                    int d2 = tob(d);
                    if(a2*b2*c2*d2 > N)
                        break;
                    for(int e = 1; e <= 31; e++){
                        int e2 = tob(e);
                        int x = a2*b2*c2*d2*e2;
                        if(x > N)
                            break;
                        m[x] = true;
                    }
                }
            }
        }
    }

        int t = 1;
    std::cin>>t;
    while(t--) solve();
}
void solve(){
    int n,m;
    std::cin>>n;
    std::vector<int> v(n);
    for (int i = 0; i < n;i++){
        std::cin>>v[i];
    }
    std::vector<int> tol(n, 0), tor(n, 0);
    tor[1] = tol[n - 2] = 1;

    for (int i = 1; i < n - 1;i++){
        int left = v[i] - v[i - 1], right = v[i + 1] - v[i];
        tor[i + 1] = tor[i] + (left > right ? 1 : right);
    }
    for (int i = n - 2;i>0;i--){
        int left = v[i] - v[i - 1], right = v[i + 1] - v[i];
        tol[i-1] = tol[i] + (left < right ? 1 : left);
    }

    std::cin >> m;
    while(m--){
        int l, r;
        std::cin>>l>>r;
        l--;
        r--;
        std::cout<< (l<r ? tor[r] - tor[l] : tol[r] - tol[l])<<"\n";
    }
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
void solve()
{
    int n, l;
    std::cin >> n >> l;
    std::vector<std::pair<int, int>> v(n);
    for (int i = 0; i < n; i++) {
        std::cin >> v[i].second >> v[i].first;
    }
    std::sort(v.begin(), v.end());
    int res = 0;
    for (int i = 0; i < n; i++){
        std::priority_queue<int> s;
        int curr = 0;
        for (int j = i; j < n; j++){
            s.push(v[j].second);
            curr += v[j].second;
            while (!s.empty() && v[j].first - v[i].first + curr > l)
            {
                int mx = s.top();
                s.pop();
                curr -= mx;
            }
            res = std::max(res, (int)s.size());
        }
    }
    std::cout << res << '\n';
}

int32_t main()
{
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t = 1;
    std::cin >> t;
    while (t--)
        solve();
}
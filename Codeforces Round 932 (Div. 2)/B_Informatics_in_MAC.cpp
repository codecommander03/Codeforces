void solve(){
    int n,x=0;
    std::cin>>n;
    std::vector<int> v(n),pre(n),suf(n),cnt(n);

    for(int i=0; i<n; i++){
        std::cin>>v[i];
    }
    for (int i = 1; i < n;i++){
        cnt[v[i - 1]]++;
        while(cnt[x])
            x++;
        pre[i]=x;
    }
    std::fill(cnt.begin(), cnt.end(), 0);
    x=0;
    for(int i=n-1; i>=0; i--){
        cnt[v[i]]++;
        while(cnt[x])
            x++;
        suf[i]=x;
    }
    for (int i = 1; i < n; i++){
        if(pre[i] == suf[i]){
            std::cout << 2 << "\n";
            std::cout << "1 " << i << "\n";
            std::cout << i + 1 << " " << n << "\n";
            return;
        }
    }
    std::cout << "-1\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
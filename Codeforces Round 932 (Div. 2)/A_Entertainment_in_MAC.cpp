void solve(){
    int n;
    std::string s;
    std::cin>>n>>s;
    int type = 2, N = s.size();
    for (int i = 0; i < N/2;i++){
        if(s[i] < s[N-1-i]){
            type = 1;
            break;
        }
        if(s[i] > s[N-1-i]){
            type = 0;
            break;
        }
    }
    if(type>=1){
        std::cout<<s<<'\n';
        return;
    }
    for (int i = N - 1; i >= 0;i--) std::cout<<s[i];
    std::cout<<s<<"\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
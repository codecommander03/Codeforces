void solve(){
    int m,n,xa,ya,xb,yb;
    std::cin>>m>>n>>xa>>ya>>xb>>yb;
    if(std::abs(ya-yb) > xb-xa){
        std::cout << "Draw\n";
        return;
    }

    int z = (xb-xa)/2;
    if((xb-xa) % 2){
        if(std::max(1LL,yb-z) < std::max(1LL,ya-z-1) || std::min(n,yb+z) > std::min(n,ya+z+1)){
            std::cout << "Draw\n";
        }
        else
            std::cout << "Alice\n";
    }
    else {
        if(std::max(1LL,ya-z) < std::max(1LL,yb-z) || std::min(n,ya+z) > std::min(n,yb+z)){
            std::cout << "Draw\n";
        }
        else
            std::cout << "Bob\n";
    }
}
 

void solve(){
    int n, f, a, b;
    std::cin>>n>>f>>a>>b;
    //std::cout << n << " " << f << " " << a << " " << b << "\n";
    int time = 0LL, rem = f, x;
    bool flag = true;
    for(int i=0; i<n; i++){
        std::cin >> x;
        int t = x - time;
        int cur = std::min(t * a, b);
        if(rem <= cur){
            //std::cout << x<<"NO\n";
            flag = false;
        }
        rem -= cur;
        time = x;
    }
    if(!flag) std::cout << "NO\n";
    else std::cout << "YES\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
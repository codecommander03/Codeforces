void solve(){
    std::string s;
    std::cin>>s;
    int n = s.size();
    for (int i = n / 2; i >=1;i--){
        if(s[i]=='0')
            continue;
        int a = std::stoi(s.substr(0, i)), b = stoi(s.substr(i, n - i));
        //std::cout << a << " " << b << "\n";
        if(a < b) {
            std::cout << a << " " << b << "\n";
            return;
        }
    }
    //std::cout << "\n";
    std::cout << "-1\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
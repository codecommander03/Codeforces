void solve(){
    std::vector<int> a(30, 0);
    int q;
    std::cin >> q;
    while(q--){
        int t, v;
        std::cin >> t >> v;
        if(t==1){
            a[v]++;
        }
        else{
            for (int i = 29; i >= 0;i--){
                int temp = std::min(a[i], v >> i);
                v -= temp << i;
            }
            if(v==0)
                std::cout << "YES\n";
            else std::cout<< "NO\n";
        }
    }
}
 

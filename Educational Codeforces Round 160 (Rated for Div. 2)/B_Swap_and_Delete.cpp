void solve(){
    std::string s;
    std::cin >> s;
    int o = 0, z = 0;

    for(char c: s){
        if(c == '0') z++;
        else o++;
    }
    int i = 0;
    for(char c: s){
        if(c == '0'){
            if(o == 0){
                std::cout << s.size() - i<<"\n";
                break;
            }
            o--;
        }
        else{
            if(z == 0){
                std::cout << s.size() - i << "\n";
                break;
            }
            z--;
        }
        i++;
    }
    if(o+z==0) std::cout << "0\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
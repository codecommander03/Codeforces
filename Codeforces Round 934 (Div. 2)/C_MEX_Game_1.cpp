void solve()
{
    int n, x;
    std::cin >> n;
    std::map<int, int> m;
    for (int i = 0; i < n; i++)
    {
        std::cin >> x;
        m[x]++;
    }
    x = 0;
    for (int i = 0; i <= 200000;i++){
        if(m[i] == 1)
            x++;
        if(m[i] == 0 || x == 2) {
            std::cout << i << '\n';
            return;
        }
    }
    std::cout << "200001\n";
}


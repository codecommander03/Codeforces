void solve(){
    int n;
    std::string s1, s2;
    std::cin>>n>>s1>>s2;
    int end=n-1, start=0;

    for (int i = n - 1; i >= 1;i--){
        if(s1[i]=='1' && s2[i-1]=='0')
            end = i-1;
    }

    for (int i = 0; i < end;i++){
        if(s2[i]=='1' && s1[i+1]=='0'){
            start = i+1;
        }
    }

    for (int i = 0; i <= end;i++){
        std::cout<<s1[i];
    }

    for (int i = end; i < n;i++){
        std::cout<<s2[i];
    }

    std::cout<<'\n'<<end-start+1<<'\n';
}
 

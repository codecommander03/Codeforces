std::string help(int n){
    std::string res;
    for (int i = 30; i >=0;i--){
        if(n&(1<<i)){
            res += "1";
        }else{
            res += "0";
        }
    }
    return res;
}
std::string help2(int n)
{
    std::string res;
    for (int i = 30; i >= 0; i--)
    {
        if (n & (1 << i))
        {
            res += "0";
        }
        else
        {
            res += "1";
        }
    }
    return res;
}
void solve()
{
    int n, x;
    std::cin >> n;
    std::map<std::string, int> m;
    int cnt = 0;
    for (int i = 0; i < n; i++)
    {
        std::cin >> x;
        if (m[help2(x)] > 0)
        {
            cnt++;
            m[help2(x)]--;
        }
        else
        {
            m[help(x)]++;
        }
    }
    std::cout << n - cnt << std::endl;
}

int32_t main()
{
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t = 1;
    std::cin >> t;
    while (t--)
        solve();
}
std::vector<int> a;
void solve(){
    int n, res = 1;
    std::cin>>n;
    for(int i: a){
        if(i<=n)
            res = i;
        else
            break;
    }
    std::cout<<res<<"\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);
    int x = 1;
    while(x<=1e10){
        a.push_back(x);
        x*=2;
    }

    int t=1;
    std::cin>>t;
    while(t--) solve();
}


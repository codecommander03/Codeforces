#include <bits/stdc++.h>

using i64 = long long;

void solve()
{
    int n;
    std::cin >> n;

    int res = 0;
    for (int i = 0; i < n;i++)
        for (int j = 0; j < n;j++)
            res += n - std::min(i, j);

    std::cout << res << " " << 2 * n << "\n";

    for (int _ = 0; _ < n;_++){
        std::cout << 1 << " " << _ + 1 << " ";
        for (int i = n;i > 0;i--)
            std::cout << i << " \n"[i == 1];

        std::cout << 2 << " " << _ + 1 << " ";
        for (int i = n; i >=1;i--)
            std::cout << i << " \n"[i == 1];
    }
}

int main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t;
    std::cin >> t;

    while (t--)
    {
        solve();
    }

    return 0;
}
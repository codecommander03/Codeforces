void solve()
{
    int n, x;
    std::cin >> n, x;
    std::vector<int> zero(n+1), one(n+1);
    for (int i = 1; i <= n; i++)
    {
        std::cin >> x;
        zero[i] = zero[i - 1] + (x == 0);
        one[i] = one[i - 1] + (x == 1);
    }
    int res = 0;
    for (int i = 1; i <= n; i++)
    {
        for (int j = i; j <= n; j++)
        {
            int zero2 = zero[j] - zero[i-1];
            int one2 = one[j] - one[i-1];
            res = std::max(res, one[n] - one2 + zero2);
            //std::cout << i << " " << j << "->" << one[n - 1] - one2 + zero2 << '\n';
        }
    }
    std::cout << res << '\n';
}

int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    //std::cin>>t;
    while(t--) solve();
}
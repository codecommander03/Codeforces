void solve(){
    std::string s;
    std::cin>>s;
    int n = s.size(), m;
    std::vector<int> v(n, 0);
    for (int i = n - 2; i >= 0;i--){
        v[i] = v[i+1] + (s[i] == s[i + 1]);
    }
    std::cin>>m;
    while(m--){
        int l, r;
        std::cin>>l>>r;
        std::cout<<v[l-1]-v[r-1]<<'\n';
    }
}
 

// "Everyone leaves in the end"

void solve(){
    int n,m;
    std::cin>>n;
    std::vector<int> v(n);
    for(int i=0; i<n; i++) std::cin>>v[i];
    std::vector<int> v1(n+1), v2(n+1);
    for(int i=0; i<n; i++){
        v1[i+1] = v1[i] + v[i];
    }
    std::sort(v.begin(), v.end());
    for(int i=0; i<n; i++){
        v2[i+1] = v2[i] + v[i];
    }
    std::cin>>m;
    while(m--){
        int t, l, r;
        std::cin>>t>>l>>r;
        if(t==1){
            std::cout<<v1[r]-v1[l-1]<<'\n';
        }else{
            std::cout<<v2[r]-v2[l-1]<<'\n';
        }
    }

}
 

void solve(){
    int n, k,res,sum=0,window=0;
    std::cin>>n>>k;
    std::vector<int> v(n),sleep(n);
    for(int i=0; i<n; i++) std::cin>>v[i];
    for(int i=0; i<n; i++){
        std::cin >> sleep[i];
        if(sleep[i]) sum += v[i];
        else if(i < k) window += v[i];
    }
    res = sum + window;
    for (int i = k; i < n;i++){
        if(sleep[i]==0) window += v[i];
        if(sleep[i-k]==0) window -= v[i-k];
        res = std::max(res, sum + window);
    }
    std::cout<<res;
}
 

bool vowel(char c){
    if(c=='a' || c=='e' || c=='i' || c=='o' || c=='u')
        return true;
    return false;
}

void solve(){
    int n;
    string s;
    cin >> n >> s;

    int i = 0;

    while(i<n){
        std::cout << s[i];
        std::cout << s[i + 1];
        if(i==n-2 || i==n-3){
            if (i == n - 3)
            {
                std::cout << s[i + 2];
            }
            break;
        }

        if(vowel(s[i+3])){
            std::cout << ".";
            i += 2;
        }
        else{
            std::cout << s[i + 2];
            std::cout << ".";
            i += 3;
        }
    }
    std::cout << "\n";
}
 

void solve(){
    int n;
    cin >> n;
    vector<int> v(n);
    int sum = 0;
    for(int &i: v){
        cin >> i;
        sum += i;
    }
    int x = sqrt(sum);

    if(x*x == sum)
        std::cout << "YES\n";
    else
        cout << "NO\n";
}
 

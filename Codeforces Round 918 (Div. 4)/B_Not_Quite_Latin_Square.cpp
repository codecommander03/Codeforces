void solve(){
    vector<vector<char>> v(3, vector<char>(3));
    for (int i = 0; i < 3;i++){
        for (int j = 0; j < 3;j++){
            cin >> v[i][j];
        }
    }

    for (int i = 0; i < 3;i++){
        bool a = false, b = false, c = false;
        for (int j = 0; j < 3;j++){
            if (v[i][j] == 'A')
                a = true;
            else if(v[i][j]=='B')
                b = true;
            else if(v[i][j]=='C')
                c = true;
        }
        if (a == false)
        {
            std::cout << "A\n";
            return;
        }
        if (b == false)
        {
            std::cout << "B\n";
            return;
        }
        if (c == false)
        {
            std::cout << "C\n";
            return;
        }
    }
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
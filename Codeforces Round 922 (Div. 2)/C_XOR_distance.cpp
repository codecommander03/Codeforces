void solve(){
    int a, b, r;
    std::cin >> a >> b >> r;

    int x = 0, first_bit = 1;
    if(a<b) std::swap(a, b);
    
    for (int i = 60; i >= 0;i--){
        int bit_a = a & (1LL << i), bit_b = b & (1LL << i);

        if(bit_a != bit_b) {
            if(first_bit){
                first_bit = 0;
            }
            else{
                if(bit_b==0 && x + (1LL << i) <= r){
                    x += (1LL << i);
                    a ^= (1LL << i);
                    b ^= (1LL << i);
                }
            }
        }
    }
    std::cout << a - b << "\n";
}
 

void solve(){
    int n;
    std::cin >> n;
    if(n==1) {
        std::cout<<"1\n";
        return;
    }
    else if(n==3){
        std::cout<<"196\n";
    }
    else{
        std::string s(n, '0');
        s[0] = '6';
        s[1] = '1';
        s[4] = '9';
        std::cout << s << '\n';
    }
    for (int i = 0; i < n/2; i++) {
        std::string s(n, '0');
        s[0] = '1';
        s[i + 1] = '6';
        s[2 * i + 2] = '9';
        std::cout<<s<<'\n';
        std::swap(s[0], s[2 * i + 2]);
        std::cout<<s<<'\n';
    }
}
 

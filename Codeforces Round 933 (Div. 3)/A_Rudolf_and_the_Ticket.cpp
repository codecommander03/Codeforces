void solve(){
    int n, m, k;
    std::cin>>n>>m>>k;
    std::vector<int> b(n), c(m);

    for(int i=0; i<n; i++) std::cin>>b[i];
    for(int i=0; i<m; i++) std::cin>>c[i];

    int res = 0;
    for (int i = 0; i < n;i++){
        for (int j = 0; j < m;j++){
            res += (b[i] + c[j] <= k);
        }
    }
    std::cout<<res<<"\n";
}
 

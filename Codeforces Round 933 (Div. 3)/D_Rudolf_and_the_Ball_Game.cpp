void solve(){
    int n, m, x, d;
    char c;
    std::cin>>n>>m>>x;
    std::set<int> s;
    s.insert(x%n);
    while(m--){
        std::cin>>d>>c;
        std::set<int> t;
        if(c=='0'){
            for(auto i:s){
                t.insert((i+d)%n);
            }
        }
        else if(c=='1'){
            for(auto i:s){
                t.insert((i-d+n)%n);
            }
        }
        else{
            for(auto i:s){
                t.insert((i+d)%n);
                t.insert((i-d+n)%n);
            }
        }
        s=t;
    }
    std::cout << s.size() << "\n";
    bool flag = false;
    for(auto i:s){
        if(i==0) flag=true;
        else std::cout<<i<<" ";
    }
    if(flag) std::cout<<n;
    std::cout << "\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
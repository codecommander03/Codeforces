void solve(){
    std::string s;
    std::cin >> s;
    
    std::vector<int> u, l;
    for (int i = 0; i < s.size(); i++) {
        if (s[i] == 'B') {
            if (!u.empty()) {
                u.pop_back();
            }
        } else if (s[i] == 'b') {
            if (!l.empty()) {
                l.pop_back();
            }
        } else if (std::isupper(s[i])) {
            u.push_back(i);
        } else {
            l.push_back(i);
        }
    }
    
    int i = 0, j = 0;
    while (i < u.size() || j < l.size()) {
        if (i < u.size() && (j == l.size() || u[i] < l[j])) {
            std::cout << s[u[i++]];
        } else {
            std::cout << s[l[j++]];
        }
    }
    std::cout << "\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
void solve(){
    int n;
    std::cin>>n;
    std::vector<std::vector<int>> v(n,std::vector<int>(n));
    std::vector<int> a(n, (1 << 30) - 1);
    for (int i = 0;i<n;i++){
        for (int j = 0;j<n;j++){
            std::cin>>v[i][j];
            if(i != j){
                a[i] &= v[i][j];
                a[j] &= v[i][j];
            }
        }
    }

    for (int i = 0; i < n;i++){
        for(int j = 0;j<n;j++){
            if(i == j)
                continue;
            if((a[i] | a[j]) != v[i][j]){
                std::cout<<"NO\n";
                return;
            }
        }
    }
    std::cout<<"YES\n";
    for (int i = 0; i < n;i++) std::cout<<a[i]<<" ";
    std::cout<<"\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
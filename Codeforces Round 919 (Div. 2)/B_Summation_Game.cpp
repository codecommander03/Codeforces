void solve(){
    int n, k, x;
    std::cin >> n >> k >> x;
    std::vector<int> v(n+1,0);
    for(int i = 1; i <= n; ++i){
        std::cin >> v[i];
    }
    std::sort(v.begin(), v.end());
    for (int i = 1; i <= n; ++i){
        v[i] += v[i - 1];
    }
    int ans = -v[n];
    for (int rem = 0; rem <= k;rem++){
        int lim = std::max(1LL*0, n - rem - x);
        int neg = v[n-rem] - v[lim];
        int pos = lim==0 ? 0 : v[lim];
        ans = std::max(ans, pos - neg);
    }
    std::cout << ans << '\n';
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
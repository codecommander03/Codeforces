void solve(){
    int n;
    std::cin >> n;
    int l = 1, r = 1e9;
    std::set<int> s;
    bool flag = true;
    while(n--){
        int type, val;
        std::cin>>type>>val;
        if(type==1){
            if(val<=r){
                l = std::max(l, val);
            }
            else{
                flag = false;
            }
        }
        else if(type==2){
            if(val>=l){
                r = std::min(r, val);
            }
            else{
                flag = false;
            }
        }
        else{
            if(val<=r && val>=l){
                s.insert(val);
            }
        }
    }
    if(!flag){
        std::cout<<0<<"\n";
        return;
    }
    int cnt = r - l + 1;
    for(auto i : s){
        if(i>=l && i<=r){
            cnt--;
        }
    }
    std::cout<<cnt<<"\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
void solve(){
    int n, k;
    std::cin>>n>>k;
    std::vector<int> v(n);
    std::iota(v.begin(), v.end(), 1);
    reverse(v.begin(), v.begin()+(n-k));
    for(int i: v) std::cout<<i<<" ";
    std::cout<<"\n";
}
 

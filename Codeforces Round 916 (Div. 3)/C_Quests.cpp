void solve(){
    int n, k;
    std::cin>>n>>k;
    std::vector<int> a(n),b(n);
    for(int &i: a) std::cin>>i;
    for(int &i: b) std::cin>>i;

    int curr = 0, res = 0,extra=0;
    for (int i = 0; i < n && i<k;i++){
        curr += a[i];
        extra = std::max(extra, b[i]);
        res = std::max(res, curr + extra * (k - i - 1));
    }
    std::cout << res << "\n";
}
 
int32_t main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    // std::cout<<std::fixed<<std::setprecision(0);

    int t=1;
    std::cin>>t;
    while(t--) solve();
}
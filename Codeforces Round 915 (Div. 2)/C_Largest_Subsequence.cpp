void solve(){
    int n;
    std::string s;
    std::cin>>n>>s;

    std::vector<int> v;
    int ans = 0;
    for (int i = 0; i < n;i++){
        while(!v.empty() && s[*v.rbegin()] < s[i]){
            v.pop_back();
        }
        v.push_back(i);
    }
    //for(char c: v) std::cout<<c<<" ";
    //std::cout<<"\n";
    int cnt = 0;
    while(cnt < v.size() && s[v[cnt]] == s[v[0]]) cnt++;
    ans = v.size() - cnt;

    // for (int i = 0; i < v.size();i++){
    //     if(i < v.size()-1-i){
    //         std::swap(s[v[i]], s[v[v.size() - 1 - i]]);
    //     }
    // }

    for (int i = 0; 2*i < v.size()-1; i++)
    {
        std::swap(s[v[i]], s[v[v.size() - 1 - i]]);
    }

    for (int i = 1; i < n;i++){
        if(s[i] < s[i-1]){
            std::cout<<"-1\n";
            return;
        }
    }

    std::cout<<ans<<"\n";
}
 
